function odsBadges(arr, max=5) {
  return (arr || []).slice(0, max).map(n => `<span class="ods-badge ods-${n}" title="ODS ${n}: ${ODS_NOMBRES[n]}">${n}</span>`).join("");
}
function tagsHTML(arr) {
  return (arr || []).map(t => `<span class="tag">${t}</span>`).join("");
}
function fmtMoney(n) {
  return "USD " + n.toLocaleString("es-GT");
}
function fmtDate(s) {
  if (!s) return "";
  const d = new Date(s);
  return d.toLocaleDateString("es-GT", { year: "numeric", month: "short", day: "numeric" });
}

